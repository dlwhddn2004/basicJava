package i_colletion;

public class CollectionsFramework {

	/*
	 *  <<CollectionsFramwork >>  --인터페이스 , list ,set 은 콜렉션을 상속받은 인터페이스
	 *  - collections 	: 다수의 데이터
	 *  - Framework		: 표준화된 프로그래밍 방식
	 *  
	 *  <<List >>
	 *  - Vector	 : ArrayList의 구버전
	 *  - ArrayList  : 저장순서 유지, 중복 허용
	 *  - LinkedList : 데이터 추가 및 삭제시간 단축
	 *  - Stack 	 : LIFO (Last IN first out)
	 *  - Queue 	 : FIFO (First in First OUt)
	 *  
	 *  << Set >> 
	 *  - HashSet 	 : 중복 허용 안함, 저장순서 유지 안함
	 *  - TreeSet 	 : 정렬, 검색 기능 향상
	 *  
	 *  << Map >> 
	 *  - HashTable  : HashMap의 구버전
	 *  - HashMap	 : 키(Key)와 갑(value)를 한쌍으로 저장
	 *  - TreeMap 	 : 정렬, 검색 기능 향상
	 */
}
